class Sesjob_Widget_FavouriteButtonController extends Engine_Content_Widget_Abstract {

  public function indexAction() {
    $viewer = Engine_Api::_()->user()->getViewer();
    $this->view->viewer_id = $viewerId = $viewer->getIdentity();
		if (empty($viewerId))
		return $this->setNoRender();
		if (!Engine_Api::_()->core()->hasSubject('sesjob_job') || !Engine_Api::_()->getApi('settings', 'core')->getSetting('sesjob.enable.favourite', 1))
		return $this->setNoRender();
		$this->view->sesjob = Engine_Api::_()->core()->getSubject('sesjob_job');
  }

}

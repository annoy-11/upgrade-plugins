class Epetition_Widget_FavouriteButtonController extends Engine_Content_Widget_Abstract {

  public function indexAction() {
    $viewer = Engine_Api::_()->user()->getViewer();
    $this->view->viewer_id = $viewerId = $viewer->getIdentity();
		if (empty($viewerId))
		return $this->setNoRender();
		if (!Engine_Api::_()->core()->hasSubject('epetition') || !Engine_Api::_()->getApi('settings', 'core')->getSetting('epetition.enable.favourite', 1))
		return $this->setNoRender();
		$this->view->epetition = Engine_Api::_()->core()->getSubject('epetition');
  }

}

class Sesrecipe_Model_DbTable_Reviewmetas extends Engine_Db_Table {

  protected $_name = 'sesrecipe_review_fields_meta';
  protected $_rowClass = 'Sesrecipe_Model_Reviewmeta';

  public function profileFieldId() {

    return $this->select()
                    ->from($this->info('name'), array('field_id'))
                    ->where('alias = ?', 'profile_type')
                    ->where('type = ?', 'profile_type')
                    ->query()
                    ->fetchColumn();
  }
  
}
class Sescontest_Widget_BreadcrumbController extends Engine_Content_Widget_Abstract {

  public function indexAction() {

    $coreApi = Engine_Api::_()->core();
    if (!$coreApi->hasSubject('contest') && !$coreApi->hasSubject('participant'))
      return $this->setNoRender();
    $this->view->subject = $coreApi->getSubject();
  }

}

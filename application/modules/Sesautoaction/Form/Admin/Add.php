class Sesautoaction_Form_Admin_Add extends Engine_Form {

  public function init() {

    $this->setTitle('Add New Bot');
    $this->setMethod('post');

    $this->addElement('Text', 'name', array(
        'label' => 'Enter the user name who you want to be added as bot on your website.',
        'allowEmpty' => false,
        'required' => true,
    ));
    $this->addElement('Hidden', 'resource_id', array());

    // Buttons
    $this->addElement('Button', 'submit', array(
        'label' => 'Add',
        'type' => 'submit',
        'ignore' => true,
        'decorators' => array('ViewHelper')
    ));

    $this->addElement('Cancel', 'cancel', array(
        'label' => 'Cancel',
        'link' => true,
        'prependText' => ' or ',
        'href' => '',
        'onClick' => 'javascript:parent.Smoothbox.close();',
        'decorators' => array(
            'ViewHelper'
        )
    ));
    $this->addDisplayGroup(array('submit', 'cancel'), 'buttons');
  }
}
